 package com.sinokj.app.store.packaging.model;
 
 import com.sinokj.code.bean.Base;
 
 public class Packaging extends Base
 {
   private static final long serialVersionUID = 3060608438603311114L;
   private String packagingFee;
   private String minForFree;
   private String packageDescription;
   private String picId;
   private String pic;
 
   public String getPackagingFee()
   {
     return this.packagingFee;
   }
   public void setPackagingFee(String packagingFee) {
     this.packagingFee = packagingFee;
   }
   public String getMinForFree() {
     return this.minForFree;
   }
   public void setMinForFree(String minForFree) {
     this.minForFree = minForFree;
   }
   public String getPackageDescription() {
     return this.packageDescription;
   }
   public void setPackageDescription(String packageDescription) {
     this.packageDescription = packageDescription;
   }
   public String getPicId() {
     return this.picId;
   }
   public void setPicId(String picId) {
     this.picId = picId;
   }
   public String getPic() {
     return this.pic;
   }
   public void setPic(String pic) {
     this.pic = pic;
   }
 }

